    PID INT NOT NULL PRIMARY KEY,
    VID INT,
    SPID INT,
    PQTY INT,
    SPRATE FLOAT,
    PDATE DATE,
    TRANCOST FLOAT,
    TOTAL FLOAT,
    RCV_EID INT,
    FOREIGN KEY (VID) REFERENCES vendors(VID),
    FOREIGN KEY (RCV_EID) REFERENCES Employee(EID),
    FOREIGN KEY (SPID) REFERENCES sparepart(SPID)
