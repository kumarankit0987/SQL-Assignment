-- create table Employee
CREATE TABLE Employee(
    EID INT NOT NULL PRIMARY KEY,
    ENAME VARCHAR(255),
    EJOB VARCHAR(255),
    EADD VARCHAR(255),
    ECONTACT INT,
    ESAL INT DEFAULT 0,
    EDOJ DATE,
    EDOL DATE DEFAULT NULL
);

